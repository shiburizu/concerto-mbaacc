current_lang = "EN"

def localize(s,v=None):
    s = s.upper()
    if s in strings:
        if current_lang in strings[s]:
            if v:
                return strings[s][current_lang] % v
            else:
                return strings[s][current_lang]
    logging.warning('ConcertoLang: MISSING "%s" (%s)' % (s,current_lang)) 
    return 'MISSING "%s" (%s)' % (s,current_lang)